#include "effect.h"

//=============================================================================
// コンストラクタ
//=============================================================================
CEffect::CEffect()
{
	m_pTexture = NULL;
	m_nLife = EFFECT_LIFE;
	m_col = D3DCOLOR_RGBA(255, 0, 0, 255);
}

//=============================================================================
// コンストラクタ
//=============================================================================
CEffect::~CEffect()
{
}

//=============================================================================
// コンストラクタ
//=============================================================================
HRESULT CEffect::Init(D3DXVECTOR3 pos, LPDIRECT3DTEXTURE9 texture)
{
	// 初期化
	CPolygon3D::Init();

	SetPos(pos);
	SetCol(m_col);
	//BindTexture(texture);
	return S_OK;
}

//=============================================================================
// コンストラクタ
//=============================================================================
void CEffect::Update()
{
	if (m_nLife > 0)
	{
		//m_col.a--;
		//SetCol(m_col);
		m_nLife--;
	}
	else
	{
		Uninit();
	}
}

//=============================================================================
// コンストラクタ
//=============================================================================
void CEffect::Draw()
{
}

//=============================================================================
// コンストラクタ
//=============================================================================
void CEffect::Uninit()
{
}

//=============================================================================
// コンストラクタ
//=============================================================================
CEffect * CEffect::Create(D3DXVECTOR3 pos, LPDIRECT3DTEXTURE9 texture)
{
	CEffect *pEffect;
	pEffect = new CEffect;
	pEffect->SetObjType(OBJ_TYPE_EFFECT);
	pEffect->Init(pos, texture);

	return pEffect;
}