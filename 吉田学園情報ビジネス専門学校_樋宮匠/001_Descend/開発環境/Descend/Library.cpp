//*****************************************************************************
// 遅延関数
//*****************************************************************************
void Delay(int nSecond)
{
	time_t now = nSecond * CLOCKS_PER_SEC + clock();

	while (now > clock());
}

//*****************************************************************************
// 指定範囲内の乱数を生成
//*****************************************************************************
int GetRandom(int min, int max)
{
	return min + (int)(rand()*(max - min + 1.0) / (1.0 + RAND_MAX));
}